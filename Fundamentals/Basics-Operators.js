// Operators: Arithmetic, logical, comparison, ternary operator, and bitwise operators
// Arthmatic operator
let a = 5;
let b = 4;
console.log(a+b)
console.log(a-b)
console.log(a*b)
console.log(a/b)
console.log(a%b)

//Logical Operator
let aa = true;
let bb = false;
console.log(aa && bb);
console.log(aa || bb)
console.log(!aa)

// Comparison operator
console.log(5 == '5');
console.log(5 === '5');
console.log(5 > 3);
console.log(4 < 7);

// Ternary operator
console.log("Ternary---------------------------------");
let age = 16;
let check = age >= 18 ? 'adult' : 'minor';
console.log(check)

//Bitwise operator
console.log("Bitwise---------------------------------");
console.log(5 & 3);
console.log(5 | 3);
console.log(5 ^ 3);
console.log(~5);   
console.log(5 << 1);
console.log(5 >> 1);
